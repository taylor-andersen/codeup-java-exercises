public class StringExercise {
    public static void main(String[] args) {

        String practice = "We don't need no education";
        System.out.println(practice);
        practice = "We don't need no thought control";
        System.out.println(practice);
        practice = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(practice);
        practice = "In windows, the main drive is usually C:\\";
        System.out.println(practice);
        practice = "I can do backslashes \\, double backslashes \\\\,";
        System.out.println(practice);
        practice = "and the amazing triple backslash \\\\\\!";
        System.out.println(practice);
    }
}

